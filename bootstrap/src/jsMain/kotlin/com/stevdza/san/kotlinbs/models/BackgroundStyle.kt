package com.stevdza.san.kotlinbs.models

enum class BackgroundStyle(val value: String) {
    Primary(value = "bg-primary"),
    Secondary(value = "bg-secondary"),
    Success(value = "bg-success"),
    Danger(value = "bg-danger"),
    Warning(value = "bg-warning"),
    Info(value = "bg-info"),
    Light(value = "bg-light"),
    Dark(value = "bg-dark")
}