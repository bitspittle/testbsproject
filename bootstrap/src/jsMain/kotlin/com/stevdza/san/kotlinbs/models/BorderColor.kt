    Primary(value = "border-primary"),
    Secondary(value = "border-secondary"),
    Success(value = "border-success"),
    Danger(value = "border-danger"),
    Warning(value = "border-warning"),
    Info(value = "border-info"),
    Dark(value = "border-dark"),
    Light(value = "border-light"),
    White(value = "border-white")
}